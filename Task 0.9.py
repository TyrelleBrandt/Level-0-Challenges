name = "Umuzi"

word = name.lower()
print(word)

if 'a' in word or 'e' in word or 'i' in word or 'o' in word or 'u' in word:
    print('Vowels: ')
else:
    print('No vowels: ')
if 'a' in word:
    print('a')
if 'e' in word:
    print('e')
if 'i' in word:
    print('i')
if 'o' in word:
    print('o')
if 'u' in word:
    print('u')
else:
    print('none')
